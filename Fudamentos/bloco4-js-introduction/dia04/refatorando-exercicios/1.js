
// Soma
function sum(a, b) {
  return a + b;
}
console.log(sum(5, 8));
// Soma

// Substrai
function sub (a, b) {
  return a - b;
}
console.log(sub(10, 4))
// Substrai

// Multiplica
function mult (a, b) {
  return a * b;
}
console.log(mult(5, 9));
// Multiplica

// Divide
function div (a, b) {
  return a / b;
}
console.log(div(10, 7));
// Divide

// Módulo
function mod (a, b) {
  return a % b;
}
console.log(mod(25, 3));
// Módulo

